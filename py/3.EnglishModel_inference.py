# strip all unwanted special characters anywhere in the text
PUNCTUATION_STRIP_RE = re.compile(r"[\-\_\!\@\#\$\%\^\&\*\(\)\+\=\?\.\,<>\"';:\[\]\{\}]")
MULTISPACE_RE = re.compile(r"\s+")

def clean_text(text: str) -> str:
    # remove special characters
    cleaned = PUNCTUATION_STRIP_RE.sub("", text)
    # collapse multiple spaces to one
    cleaned = MULTISPACE_RE.sub(" ", cleaned)
    # trim leading/trailing spaces
    return cleaned.strip()

def is_nouny_text(text):
    doc = nlp(text)
    return any(t.pos_ in ("NOUN","PROPN") for t in doc)

def canonicalize(aspect, lexicon=None):
    doc = nlp(aspect.lower().strip())
    heads = [t.lemma_ for t in doc if t.pos_ in ("NOUN","PROPN")]
    base = heads[-1] if heads else aspect.lower().strip()
    if lexicon and base in lexicon:
        return lexicon[base]
    return base

def refine_aspects(aspects, original_sentence=None, enforce_verbatim=False,
                   sim_dedupe_threshold=0.80, lexicon=None):
    if not aspects or not isinstance(aspects, list):
        return []

    # 1) clean + POS
    pool = []

    for a in aspects:
        a = str(a).strip()
        if not a or NUMERIC_RE.match(a):
            continue
        a = clean_text(a)
        if not is_nouny_text(a):
            continue
        pool.append(a)

    # 2) enforce explicitness vs original (optional, recommended)
    if enforce_verbatim and original_sentence:
        keep = []
        s = original_sentence.lower()
        for a in pool:
            low = a.lower()
            if low in s:
                keep.append(a)
                continue
            # head-noun fallback
            doc = nlp(a)
            head = next((t.text for t in doc if t.pos_ in ("NOUN","PROPN")), doc[-1].text)
            if head.lower() in s:
                keep.append(a)
        pool = keep

    if not pool:
        return []

    # 3) canonicalize
    canon_pairs = [(a, canonicalize(a, lexicon)) for a in pool]

    # 4) dedup by canonical form
    seen, refined = set(), []
    for raw, can in canon_pairs:
        if can not in seen:
            seen.add(can)
            refined.append(raw)

    # 5) optional fuzzy collapse of near-duplicates (token-set-ish)
    final = []
    for cand in refined:
        add = True
        for ex in final:
            sim = SequenceMatcher(None, cand.lower(), ex.lower()).ratio()
            if sim >= sim_dedupe_threshold:
                # prefer shorter canonical (tend to be more general)
                if len(canonicalize(cand)) < len(canonicalize(ex)):
                    final.remove(ex)
                    final.append(cand)
                add = False
                break
        if add:
            final.append(cand)

    return final

