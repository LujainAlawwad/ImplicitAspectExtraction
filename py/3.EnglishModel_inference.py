import pandas as pd


def evaluate(df, gold_col, prediction_col, match_type="partial", sep_token="[sep]"):
    def normalize(text):
        return text.lower().strip()

    def is_match(pred, gold_terms, exact=False):
        pred = normalize(pred)
        for gold in gold_terms:
            gold = normalize(gold)
            if exact:
                if pred == gold:
                    return True
            else:
                if pred in gold or gold in pred:
                    return True
        return False

    exact = (match_type == "exact")

    TP = FP = FN = 0

    for _, row in df.iterrows():
        gold = row.get(gold_col, None)
        pred_str = row.get(prediction_col, "")
        # if pred_str is list, just retrieve the first item:
        # if isinstance(pred_str, list):
        #     pred_str = pred_str[0]
        #     # new (safe):
        if isinstance(pred_str, list):
            pred_str = next((x for x in pred_str if isinstance(x, str) and x.strip()), "none")

        if not isinstance(gold, list):
            continue  # Skip rows with no aspectTerms

        gold_terms = [normalize(t) for t in gold]

        if not isinstance(pred_str, str) or pred_str.strip() == "":
            FN += len(gold_terms)
            continue

        predicted_terms = [normalize(p) for p in pred_str.split(sep_token) if p.strip()]
        matched_gold = set()

        for pred in predicted_terms:
            if is_match(pred, gold_terms, exact=exact):
                TP += 1
                for i, gold in enumerate(gold_terms):
                    if i not in matched_gold and (pred == gold if exact else pred in gold or gold in pred):
                        matched_gold.add(i)
                        break
            else:
                FP += 1

        FN += len(gold_terms) - len(matched_gold)

    precision = TP / (TP + FP) if (TP + FP) > 0 else 0.0
    recall = TP / (TP + FN) if (TP + FN) > 0 else 0.0
    f1 = 2 * precision * recall / (precision + recall) if (precision + recall) > 0 else 0.0
    accuracy = TP / (TP + FP + FN) if (TP + FP + FN) > 0 else 0.0

    return {
        "Match Type": match_type,
        "Precision": round(precision * 100, 2),
        "Recall": round(recall * 100, 2),
        "F1": round(f1 * 100, 2),
        "Accuracy": round(accuracy * 100, 2),
        "TP": TP,
        "FP": FP,
        "FN": FN,
    }

