def iterative_residual_decoding2(sentence, max_rounds=3):
    """
    Iterative Residual Decoding with:
      - filtering to aspects that appear in the original sentence
      - POS-based removal of adjective/verb-only spans
      - simple overlap cleanup at the end
    """
    original_sentence = sentence
    remaining_sentence = sentence
    all_aspects = []
    seen_norm = set()

    for round_id in range(max_rounds):
        aspects = extract_aspect(remaining_sentence)  # returns list
        if not aspects:
            break

        # basic cleanup
        new_aspects = [a.strip() for a in aspects if a and a.strip()]
        if not new_aspects or all(a.lower() == "none" for a in new_aspects):
            break

        # keep only those that actually occur in the original sentence
        new_aspects = filter_to_sentence(new_aspects, original_sentence)

        # POS-based filtering: drop adjective-only / verb-only spans
        new_aspects = [a for a in new_aspects if _ig_is_valid_span(a)]

        if not new_aspects:
            # nothing useful this round
            break

        for asp in new_aspects:
            norm = normalize_aspect(asp)
            if norm not in seen_norm:
                seen_norm.add(norm)
                all_aspects.append(asp)  # keep original surface form

            # remove exact form from remaining sentence (case-insensitive)
            pattern = r"\b{}\b".format(re.escape(asp))
            remaining_sentence = re.sub(
                pattern, "", remaining_sentence, flags=re.IGNORECASE
            ).strip()

        if not remaining_sentence:
            break

    # drop literal "none" unless it's the only thing we ever saw
    final_aspects = [
        a for a in all_aspects
        if a.lower() != "none" or len(all_aspects) == 1
    ]

    # overlap cleanup inside IG outputs
    final_aspects = _ig_overlap_cleanup(final_aspects)

    return final_aspects

