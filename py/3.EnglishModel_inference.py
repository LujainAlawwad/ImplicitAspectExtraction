# ------------------ NEW HELPERS FOR IG ------------------ #

def _ig_norm_for_overlap(s: str) -> str:
    """
    Normalize aspect strings for overlap comparison.
    Prefer the global _normalize_for_verbatim if available
    (already defined in the paraphrase component), otherwise
    fall back to simple lowercase + whitespace collapse.
    """
    if "_normalize_for_verbatim" in globals():
        return _normalize_for_verbatim(s)
    s = s.lower()
    s = re.sub(r"\s+", " ", s).strip()
    return s


def _ig_is_valid_span(asp: str) -> bool:
    """
    POS-based filter for IG candidates:
      - Reject single-token ADJ / ADV / VERB / AUX (pure adjectives/actions).
      - Require at least one NOUN/PROPN somewhere in the span.
    If spaCy nlp is not available for any reason, we fall back to keeping the span.
    """
    asp = asp.strip()
    if not asp:
        return False

    # If spaCy isn't available, don't block anything here.
    if "nlp" not in globals():
        return True

    doc = nlp(asp)

    # Single-word adjective/verb/etc → drop (e.g. "speedy", "cooked", "works")
    if len(doc) == 1 and doc[0].pos_ in {"ADJ", "ADV", "VERB", "AUX"}:
        return False

    # Require at least one noun/proper noun somewhere in the phrase
    has_noun = any(t.pos_ in {"NOUN", "PROPN"} for t in doc)
    if not has_noun:
        return False

    return True


def _ig_overlap_cleanup(aspects):
    """
    Simple overlap cleanup inside IG predictions:
      - If one normalized aspect is a substring of another, keep only the longer one.
      - Uses _ig_norm_for_overlap for robust comparison.
    This is purely intra-IG (does not look at baseline/KG).
    """
    if not aspects:
        return []

    norms = [_ig_norm_for_overlap(a) for a in aspects]
    keep = [True] * len(aspects)

    for i, ni in enumerate(norms):
        if not keep[i] or not ni:
            continue
        for j, nj in enumerate(norms):
            if i == j or not keep[j] or not nj:
                continue
            # substring relation → drop the shorter surface form
            if ni in nj or nj in ni:
                if len(aspects[i]) < len(aspects[j]):
                    keep[i] = False
                    break
                else:
                    keep[j] = False

    return [asp for asp, k in zip(aspects, keep) if k]


