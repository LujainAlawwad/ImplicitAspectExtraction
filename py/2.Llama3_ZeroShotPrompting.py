def llama3_generate(prompt: str, retries: int = 3, delay: int = 5):
    for attempt in range(retries):
        try:
            output = replicate.run(
                "meta/meta-llama-3-8b-instruct",
                input={"prompt": prompt}
            )
            return "".join(output)
        except Exception as e:
            print(f"⚠️ Error: {e} | Attempt {attempt+1}/{retries}")
            time.sleep(delay)
    return "[ERROR]"

