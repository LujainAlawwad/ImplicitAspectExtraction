# ================================================================
# Evaluation Block — Strict & Relaxed
# Paste this AFTER the multi-dataset inference loop above
#
# Strict:  predicted ASPECT must exactly match gold ASPECT
# Relaxed: predicted ASPECT matches gold ASPECT by head-noun
#          or token-set inclusion (e.g. "swimming pool" matches "pool")
# Matching: ONE-TO-ONE via bipartite matching (Kuhn's algorithm)
# Scope:   Evaluated on IMPLICIT rows only
# ================================================================

import re, ast
import pandas as pd
from collections import defaultdict
from typing import List, Tuple, Dict

# ================================================================
# A. Normalisation helpers
# ================================================================
GENERAL_SYNONYMS = {
    "misc", "miscellaneous", "anecdotes/miscellaneous",
    "miscellany", "other", "general"
}

_IRREGULAR_SINGULAR = {
    "rooms": "room", "men": "man", "women": "woman",
    "children": "child", "people": "person",
    "facilities": "facility", "amenities": "amenity",
    "services": "service", "batteries": "battery",
    "dishes": "dish", "buses": "bus", "watches": "watch",
    "indices": "index", "matrices": "matrix",
    "prices": "price", "drinks": "drink",
}

def _singularize_token(tok: str) -> str:
    t = tok.lower().strip()
    if t in _IRREGULAR_SINGULAR:
        return _IRREGULAR_SINGULAR[t]
    if re.search(r'[^aeiou]ies$', t):
        return re.sub(r'ies$', 'y', t)
    if re.search(r'(sses|zzes|ches|shes|xes)$', t):
        return t[:-2]
    if t.endswith('s') and not t.endswith('ss') and len(t) > 3:
        return t[:-1]
    return t

_token_re = re.compile(r"[a-z0-9]+")

def _tokset(s: str) -> List[str]:
    """Tokenise + singularize."""
    return [_singularize_token(x) for x in _token_re.findall((s or "").lower())]

def _head(s: str) -> str:
    """Last meaningful token after singularization."""
    toks = _tokset(s)
    return toks[-1] if toks else ""

def _norm_field(s: str) -> str:
    """Normalise a field: lowercase, singularize, join."""
    return " ".join(_tokset(s))

def _norm_aspect(s: str) -> str:
    """Normalise aspect string + canonicalise general synonyms."""
    n = _norm_field(s)
    return "general" if n in GENERAL_SYNONYMS else n

# ================================================================
# B. Gold parsing
#    Accepts: "FOOD#QUALITY" / "['FOOD#QUALITY','SERVICE#GENERAL']"
#    Returns: [(entity_norm, aspect_norm), ...]
# ================================================================
def _parse_gold(val) -> List[Tuple[str, str]]:
    if val is None:
        return []
    if isinstance(val, list):
        raw = val
    else:
        s = str(val).strip()
        try:
            v = ast.literal_eval(s)
            raw = v if isinstance(v, list) else [s]
        except Exception:
            raw = [s] if s else []

    pairs = []
    for x in raw:
        xs = str(x).strip()
        if not xs or xs.lower() in ('nan', 'none', ''):
            continue
        if "#" in xs:
            e, a = xs.split("#", 1)
            e = _norm_field(e)
            a = _norm_aspect(a)
        else:
            e = ""
            a = _norm_aspect(xs)
        if e or a:
            pairs.append((e, a))
    return pairs

# ================================================================
# C. Prediction parsing
#    kg_final_pairs: ["FOOD#QUALITY", "RESTAURANT#GENERAL"]
# ================================================================
def _parse_preds(val) -> List[Tuple[str, str]]:
    if val is None:
        return []
    if isinstance(val, list):
        raw = val
    else:
        s = str(val).strip()
        try:
            v = ast.literal_eval(s)
            raw = v if isinstance(v, list) else [s]
        except Exception:
            raw = [s] if s else []

    pairs = []
    for x in raw:
        xs = str(x).strip()
        if not xs or xs.lower() in ('nan', 'none', ''):
            continue
        if "#" in xs:
            e, a = xs.split("#", 1)
            e = _norm_field(e)
            a = _norm_aspect(a)
            pairs.append((e, a))
    return pairs

# ================================================================
# D. Compatibility predicates
#
# Strict:  predicted aspect == gold aspect (exact after norm)
# Relaxed: exact OR head-noun equality OR token-set inclusion
#          e.g. "swimming pool" matches "pool" ✅
#               "food quality"  matches "quality" ✅
#               "style options" matches "style" ✅
#
# Entity match: used ONLY as fallback when aspect is 'general'
#   on both sides (avoids entity noise polluting aspect matching)
# ================================================================
def _same_strict(a: str, b: str) -> bool:
    return _norm_field(a) == _norm_field(b)

def _same_relaxed(a: str, b: str) -> bool:
    """Aspect relaxed match: exact / head-noun / token-set inclusion."""
    a = _norm_field(a)
    b = _norm_field(b)
    if a == b:
        return True
    # head-noun equality (last token after singularization)
    ha, hb = _head(a), _head(b)
    if ha and ha == hb:
        return True
    # token-set inclusion (partial match)
    sa, sb = set(a.split()), set(b.split())
    if sa and sb and (sa.issubset(sb) or sb.issubset(sa)):
        return True
    return False

def _compatible(
    pred_pair: Tuple[str, str],
    gold_pair: Tuple[str, str],
    relaxed: bool = False,
) -> bool:
    """
    A prediction is compatible with a gold pair when:
      - Aspect matches (strict or relaxed)
      - OR both aspects are 'general' and entities match
        (handles RESTAURANT#GENERAL vs FOOD#GENERAL edge case)
    """
    pe, pa = pred_pair
    ge, ga = gold_pair

    # Primary: aspect-driven match
    if relaxed:
        if _same_relaxed(pa, ga):
            return True
    else:
        if _same_strict(pa, ga):
            return True

    # Secondary: entity match only when both aspects are general
    if pa == "general" and ga == "general":
        if ge and pe == ge:
            return True

    return False

# ================================================================
# E. Bipartite matching (Kuhn's algorithm)
#    ONE-TO-ONE: each prediction matches at most one gold,
#                each gold matched at most once
# ================================================================
def _max_bipartite_matching(
    adj: List[List[int]],
    n_left: int,
    n_right: int,
) -> List[Tuple[int, int]]:
    matchR = [-1] * n_right

    def dfs(u, seen):
        for v in adj[u]:
            if not seen[v]:
                seen[v] = True
                if matchR[v] == -1 or dfs(matchR[v], seen):
                    matchR[v] = u
                    return True
        return False

    for u in range(n_left):
        dfs(u, [False] * n_right)

    return [(u, v) for v, u in enumerate(matchR) if u != -1]

# ================================================================
# F. Single-dataset evaluation block
# ================================================================
def _eval_block(
    df: pd.DataFrame,
    pred_col: str = "kg_final_pairs",
    mode_name: str = "STRICT",
    relaxed: bool = False,
    implicit_only: bool = True,
) -> Tuple[float, float, float, List[str]]:
    """
    Runs evaluation on df (filtered to implicit rows if implicit_only=True).
    Returns (precision, recall, f1, per_row_labels).
    Prints full metrics report.
    """
    if implicit_only and 'type' in df.columns:
        eval_df = df[df['type'] == 'implicit'].copy()
    else:
        eval_df = df.copy()

    TP = FP = FN = 0
    any_tp_rows  = 0
    rows         = len(eval_df)
    per_row_labels = []

    for _, r in eval_df.iterrows():
        # Parse predictions
        pred_pairs = _parse_preds(r.get(pred_col))

        # Parse gold — deduplicate identical pairs within row
        gold_raw = _parse_gold(r.get("category"))
        seen_g = set()
        gold_pairs = []
        for g in gold_raw:
            if g not in seen_g:
                seen_g.add(g)
                gold_pairs.append(g)

        # Skip rows with no gold (e.g. objective rows without annotation)
        if not gold_pairs:
            per_row_labels.append("SKIP")
            continue

        if not pred_pairs:
            # No prediction → all gold are FN
            FN += len(gold_pairs)
            per_row_labels.append("FN")
            continue

        # Build adjacency matrix
        adj = [
            [
                j for j, g in enumerate(gold_pairs)
                if _compatible(p, g, relaxed=relaxed)
            ]
            for p in pred_pairs
        ]

        matched_pairs = _max_bipartite_matching(adj, len(pred_pairs), len(gold_pairs))
        matched_count = len(matched_pairs)

        TP += matched_count
        FP += len(pred_pairs) - matched_count
        FN += len(gold_pairs) - matched_count

        row_has_tp = matched_count > 0
        if row_has_tp:
            any_tp_rows += 1

        if matched_count == len(pred_pairs) and matched_count == len(gold_pairs):
            row_lab = "TP"
        elif row_has_tp:
            row_lab = "FP"
        else:
            row_lab = "FN"
        per_row_labels.append(row_lab)

    num_preds = TP + FP
    prec = TP / num_preds      if num_preds      else 0.0
    rec  = TP / (TP + FN)      if (TP + FN)      else 0.0
    f1   = 2*prec*rec/(prec+rec) if (prec + rec) else 0.0
    hit  = any_tp_rows / rows  if rows           else 0.0

    print(f"\n  === {mode_name} ({'implicit only' if implicit_only else 'all rows'}) ===")
    print(f"    Rows evaluated : {rows}")
    print(f"    TP             : {TP}")
    print(f"    FP             : {FP}")
    print(f"    FN             : {FN}")
    print(f"    Precision      : {prec:.4f}")
    print(f"    Recall         : {rec:.4f}")
    print(f"    F1             : {f1:.4f}")
    print(f"    Any-TP@row     : {hit:.4f}  ({any_tp_rows}/{rows} rows with ≥1 correct pred)")

    return prec, rec, f1, per_row_labels

