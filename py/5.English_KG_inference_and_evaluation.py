# 9. Semantic index — ENTITY#ASPECT compound embeddings
#    Built ONCE — shared across all datasets
# ================================================================
def build_semantic_index(kg_index: Dict, encoder) -> Dict:
    compound_to_aspect: Dict[str, str] = {}
    compounds: List[str] = []

    for asp, ents in kg_index["aspect_to_entities"].items():
        if not ents:
            compounds.append(asp)
            compound_to_aspect[asp] = asp
        else:
            for ent in ents:
                comp = f"{ent}#{asp}"
                compounds.append(comp)
                compound_to_aspect[comp] = asp

    entities = list(kg_index["entity_to_domain"].keys())

    print("Encoding ENTITY#ASPECT compounds...")
    comp_embs = encoder.encode(
        compounds, normalize_embeddings=True,
        convert_to_numpy=True, show_progress_bar=True,
    )
    print("Encoding entities...")
    ent_embs = encoder.encode(
        entities, normalize_embeddings=True,
        convert_to_numpy=True, show_progress_bar=False,
    )

    print(f"✅ Semantic index built:")
    print(f"   Compounds : {len(compounds)}")
    print(f"   Entities  : {len(entities)}")

    return {
        "compounds":          compounds,
        "compound_to_aspect": compound_to_aspect,
        "comp_embs":          comp_embs,
        "entities":           entities,
        "ent_embs":           ent_embs,
        "ent_idx":            {e: i for i, e in enumerate(entities)},
    }

sem_index = build_semantic_index(kg_index, encoder)

