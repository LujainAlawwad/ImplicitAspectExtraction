def dedup_gold_field(val) -> list:
    """
    Parse a gold field (aspect or category) and deduplicate
    while preserving order of first occurrence.
    Works on both string representations and actual lists.

    Examples:
      "['product', 'product']"         → ['product']
      "['food#general', 'food#general']" → ['food#general']
      "['food#quality', 'food#general']" → ['food#quality', 'food#general']  (unchanged)
    """
    # Parse to list
    if isinstance(val, list):
        items = [str(v).strip() for v in val]
    else:
        s = str(val).strip()
        if s.startswith('['):
            try:
                parsed = ast.literal_eval(s)
                items = [str(v).strip() for v in parsed]
            except Exception:
                items = [s]
        else:
            items = [s]

    # Deduplicate preserving order — use lower() key for comparison
    seen = set()
    deduped = []
    for item in items:
        key = item.strip().lower()
        if key and key not in seen:
            seen.add(key)
            deduped.append(item)

    return deduped if deduped else ['none']

# ── Prediction cleaner ────────────────────────────────────────────
def clean_predicted_aspects(raw_pred,
                             use_camel: bool = True,
                             camel_obj: Optional[CamelAdjRemover] = None
                             ) -> List[str]:
    if raw_pred is None:
        aspects = []
    elif isinstance(raw_pred, list):
        aspects = raw_pred
    else:
        aspects = parse_aspect_string(raw_pred)

    if use_camel:
        try:
            camel = camel_obj or CamelAdjRemover()
            stripped = [camel.strip_adjectives(a) for a in aspects]
        except Exception:
            stripped = [strip_adjectives_heuristic(a) for a in aspects]
    else:
        stripped = [strip_adjectives_heuristic(a) for a in aspects]

    seen = set()
    final = []
    for a in stripped:
        a = str(a).strip()
        if not a:
            continue
        key = normalize_phrase(a)
        if key and key not in seen:
            seen.add(key)
            final.append(a)
    return final

# ── Matching ──────────────────────────────────────────────────────
def jaccard(a: str, b: str) -> float:
    A = set(a.split())
    B = set(b.split())
    if not A and not B: return 1.0
    if not A or not B:  return 0.0
    return len(A & B) / len(A | B)

def similarity(gold: str, pred: str) -> float:
    g = normalize_phrase(gold)
    p = normalize_phrase(pred)
    if not g or not p: return 0.0
    if g == p:         return 1.0
    if g in p or p in g: return 1.0
    return jaccard(g, p)

def best_alignment(gold: List[str], pred: List[str],
                   thresh: float = 0.6) -> Tuple[int, List]:
    pairs = []
    for i, g in enumerate(gold):
        for j, p in enumerate(pred):
            s = similarity(g, p)
            if s >= thresh:
                pairs.append((i, j, s))
    pairs.sort(key=lambda x: x[2], reverse=True)
    matched_g, matched_p, matches = set(), set(), []
    for gi, pj, s in pairs:
        if gi not in matched_g and pj not in matched_p:
            matched_g.add(gi); matched_p.add(pj)
            matches.append((gi, pj, s))
    return len(matches), matches

def prf1_for_example(gold_terms: Iterable[str],
                     pred_terms: Iterable[str],
                     thresh: float = 0.6) -> Dict:
    gold = [t for t in map(str, gold_terms) if t and t.strip()]
    pred = [t for t in map(str, pred_terms) if t and t.strip()]
    if not gold and not pred:
        return {"tp":0,"fp":0,"fn":0,"precision":1.0,"recall":1.0,"f1":1.0}
    tp, _ = best_alignment(gold, pred, thresh=thresh)
    fp = max(0, len(pred) - tp)
    fn = max(0, len(gold) - tp)
    prec = tp/(tp+fp) if (tp+fp) else 0.0
    rec  = tp/(tp+fn) if (tp+fn) else 0.0
    f1   = 2*prec*rec/(prec+rec) if (prec+rec) else 0.0
    return {"tp":tp,"fp":fp,"fn":fn,"precision":prec,"recall":rec,"f1":f1}

# ── evaluate_dataframe (original, unchanged) ──────────────────────
def evaluate_dataframe(df: pd.DataFrame,
                       pred_col: str = "prediction",
                       gold_col: str = "target",
                       use_camel: bool = False,
                       jaccard_threshold: float = 0.6):
    df = df.copy()
    df["pred_list_clean"] = df[pred_col].apply(
        lambda s: clean_predicted_aspects(s, use_camel=use_camel)
    )

    def parse_gold(x):
        if isinstance(x, list): return x
        return parse_aspect_string(x)
    df["gold_list"] = df[gold_col].apply(parse_gold)

    per = df.apply(
        lambda r: prf1_for_example(
            r["gold_list"], r["pred_list_clean"], thresh=jaccard_threshold
        ), axis=1
    )
    df["precision"] = per.apply(lambda m: m["precision"])
    df["recall"]    = per.apply(lambda m: m["recall"])
    df["f1"]        = per.apply(lambda m: m["f1"])
    df["tp"]        = per.apply(lambda m: m["tp"])
    df["fp"]        = per.apply(lambda m: m["fp"])
    df["fn"]        = per.apply(lambda m: m["fn"])
    df["support"]   = df["gold_list"].apply(len)

    tot_tp = df["tp"].sum(); tot_fp = df["fp"].sum(); tot_fn = df["fn"].sum()
    micro_p = tot_tp/(tot_tp+tot_fp) if (tot_tp+tot_fp) else 0.0
    micro_r = tot_tp/(tot_tp+tot_fn) if (tot_tp+tot_fn) else 0.0
    micro_f = 2*micro_p*micro_r/(micro_p+micro_r) if (micro_p+micro_r) else 0.0

    macro_p = df["precision"].mean() if len(df) else 0.0
    macro_r = df["recall"].mean()    if len(df) else 0.0
    macro_f = df["f1"].mean()        if len(df) else 0.0

    total_support = df["support"].sum()
    if total_support > 0:
        weighted_p = (df["precision"]*df["support"]).sum()/total_support
        weighted_r = (df["recall"]   *df["support"]).sum()/total_support
        weighted_f = (df["f1"]       *df["support"]).sum()/total_support
    else:
        weighted_p = weighted_r = weighted_f = 0.0

    return {
        "micro": {
            "precision": round(float(micro_p),4),
            "recall":    round(float(micro_r),4),
            "f1":        round(float(micro_f),4),
            "accuracy":  round(float(tot_tp/(tot_tp+tot_fp+tot_fn)),4)
                         if (tot_tp+tot_fp+tot_fn) else 0.0,
            "tp": int(tot_tp), "fp": int(tot_fp), "fn": int(tot_fn),
        },
        "macro": {
            "precision": round(float(macro_p),4),
            "recall":    round(float(macro_r),4),
            "f1":        round(float(macro_f),4),
        },
        "weighted_macro": {
            "precision": round(float(weighted_p),4),
            "recall":    round(float(weighted_r),4),
            "f1":        round(float(weighted_f),4),
        },
        "df": df,
    }

