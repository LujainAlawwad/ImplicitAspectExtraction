VERB_CLUES_TIER1: Set[str] = {
    # Strong sentiment verbs (existing)
    "love", "loved", "loving",
    "recommend", "recommended", "recommending",
    "return", "returned", "returning",
    "comeback", "impressed", "impress",
    "satisfied", "pleased", "delighted", "thrilled",
    "obsessed", "hooked", "addicted",
    "disappoint", "disappointed", "disappointing",
    "dislike", "disliked", "regret", "regretted",
    "hate", "hated", "avoid", "avoided", "avoiding",
    "ignored", "ignore", "neglected", "neglect",
    "worth",
    "miss", "missed", "missing",
    # ── Pattern 2: action verbs expressing overall satisfaction ──
    # Positive repurchase intent → strong GENERAL signal
    "reorder", "reordered", "reordering",
    "repurchase", "repurchased",
    # "order" / "buy" / "purchase" alone are weak — only added
    # in conjunction with "again" or "more" (handled in scan below)
}

VERB_CLUES_TIER2: Set[str] = {
    "like", "liked",
    "enjoy", "enjoyed", "enjoying",
    "suggest", "suggested",
    "tried", "try",
    "waited", "wait", "waiting",
    "went", "go",
    "visit", "visited",
    # Action verbs (weaker — need "again"/"more"/"never" to fire)
    "order", "ordered", "ordering",
    "buy", "bought", "purchase", "purchased",
    "use", "used", "using",
}

NEGATION_WORDS: Set[str] = {
    "not", "never", "don't", "dont", "won't", "wont",
    "wouldn't", "wouldnt", "can't", "cant", "no",
}

# ── Words that strengthen action verbs into GENERAL signals ──────
# "will order AGAIN" / "buy MORE" → positive intent
# "NEVER buy again" → negative intent (still GENERAL)
ACTION_AMPLIFIERS: Set[str] = {
    "again", "more", "another", "always", "definitely",
    "certainly", "absolutely", "forever", "regularly",
}


def _scan_verb_clues(sentence: str) -> int:
    """
    Scans sentence for sentiment verb clues.
    Returns:
        1 — Tier 1 verb found (strong GENERAL signal)
        2 — Tier 2 verb only (weaker, context-dependent)
        0 — no sentiment verb found

    Pattern 2 fix:
        Action verbs (order/buy/purchase/use) in Tier 2 are promoted
        to Tier 1 strength when combined with an amplifier or negation,
        because:
          "will order again"  → strong positive GENERAL
          "never buy again"   → strong negative GENERAL
          "always use this"   → strong positive GENERAL
          "ordered three"     → too weak — no signal (stays Tier 2)
    """
    words = set(re.findall(r"\b\w+\b", sentence.lower()))

    # Tier 1 exact match (includes reorder/repurchase)
    if words & VERB_CLUES_TIER1:
        return 1

    # Pattern 2: action verbs promoted to Tier 1 with amplifier/negation
    action_hits = words & {"order", "ordered", "ordering",
                           "buy", "bought", "purchase", "purchased",
                           "use", "used", "using"}
    if action_hits:
        has_amplifier = bool(words & ACTION_AMPLIFIERS)
        has_negation  = bool(words & NEGATION_WORDS)
        if has_amplifier or has_negation:
            return 1   # promoted: "will order again" / "never buy"

    # Remaining Tier 2
    tier2_hits = words & VERB_CLUES_TIER2
    if tier2_hits:
        strong_tier2 = tier2_hits - {"go", "like", "liked", "tried", "try",
                                      "order", "ordered", "ordering",
                                      "buy", "bought", "purchase", "purchased",
                                      "use", "used", "using"}
        has_negation = bool(words & NEGATION_WORDS)
        has_exclaim  = "!" in sentence
        if strong_tier2:
            return 2
        if (has_negation or has_exclaim) and tier2_hits:
            return 2

    return 0

