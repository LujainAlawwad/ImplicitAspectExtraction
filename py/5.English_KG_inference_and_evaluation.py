def _norm(s: str) -> str:
    if not isinstance(s, str):
        return ""
    return re.sub(r"\s+", " ", s.strip().lower())

# ================================================================
# 5. Load and index the KG
#    - describes_aspect only (indicates_implicit_aspect disabled)
#    - all domain names normalised to lowercase at index time
# ================================================================
def load_english_kg(kg_path: str) -> Dict:
    with open(kg_path, "r", encoding="utf-8") as f:
        raw = json.load(f)

    clue_to_aspects: Dict[str, List[Tuple[str, str]]] = defaultdict(list)
    for r in raw["relationships"]:
        if r["predicate"] == "describes_aspect":
            clue = _norm(r["subject"])
            asp  = _norm(r["object"])
            if clue and asp:
                clue_to_aspects[clue].append((asp, r["predicate"]))

    aspect_to_entities: Dict[str, Set[str]] = defaultdict(set)
    for a in raw["aspects"]:
        asp = _norm(a["name"])
        for e in a.get("entity", []):
            aspect_to_entities[asp].add(_norm(e))

    entity_to_domain: Dict[str, str] = {}
    for e in raw["entities"]:
        entity_to_domain[_norm(e["name"])] = e["domain"].strip().lower()

    domain_to_entities: Dict[str, Set[str]] = defaultdict(set)
    for ent, dom in entity_to_domain.items():
        domain_to_entities[dom].add(ent)

    aspect_to_domains: Dict[str, Set[str]] = defaultdict(set)
    for asp, ents in aspect_to_entities.items():
        for ent in ents:
            dom = entity_to_domain.get(ent)
            if dom:
                aspect_to_domains[asp].add(dom)

    print(f"✅ English KG loaded (describes_aspect only):")
    print(f"   Clues   : {len(clue_to_aspects)}")
    print(f"   Aspects : {len(aspect_to_entities)}")
    print(f"   Entities: {len(entity_to_domain)}")
    print(f"   Domains : {sorted(domain_to_entities.keys())}")

    return {
        "raw":                raw,
        "clue_to_aspects":    dict(clue_to_aspects),
        "aspect_to_entities": dict(aspect_to_entities),
        "entity_to_domain":   entity_to_domain,
        "domain_to_entities": dict(domain_to_entities),
        "aspect_to_domains":  dict(aspect_to_domains),
    }

