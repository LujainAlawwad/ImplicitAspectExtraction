def _extract_aspect_from_category(cat_list: List[str]) -> List[str]:
    aspects = []
    for c in cat_list:
        s = str(c).strip()
        if _is_null_value(s) or s == 'none': continue
        if '#' in s:
            asp = s.split('#',1)[1].strip().lower().replace('_',' ')
        else:
            tokens = s.strip().split()
            if len(tokens) >= 2:
                asp = ' '.join(tokens[1:]).replace('_',' ')
            elif len(tokens) == 1:
                asp = tokens[0].replace('_',' ')
            else:
                continue
        asp = _norm_asp_eval(asp)
        if asp: aspects.append(asp)
    return aspects if aspects else ['none']

def _get_combined_pred_for_row(row: pd.Series) -> List[str]:
    typ = str(row.get('type','explicit')).lower()
    if typ == 'implicit':
        pred = row.get('cleaned_combined_aspects_kg', ['none'])
        return pred if isinstance(pred, list) else ['none']
    base = row.get('cleaned_explicit_paraphrases', ['none'])
    if not isinstance(base, list): base = ['none']
    base_real   = [v for v in base if not _is_null_value(v) and v != 'none']
    kg_contrib  = aggregate_kg(row)
    kg_specific = [a for a in kg_contrib if a != 'general']
    kg_new      = [a for a in kg_specific if a not in base_real]
    combined    = base_real + kg_new
    return combined if combined else ['none']

def _get_gold_for_row(row: pd.Series) -> List[str]:
    typ = str(row.get('type','explicit')).lower()
    if typ == 'implicit':
        cat = row.get('category', ['none'])
        if not isinstance(cat, list): cat = ['none']
        return _extract_aspect_from_category(cat)
    gold = row.get('aspect', ['none'])
    return gold if isinstance(gold, list) else ['none']

def evaluate_combined(df: pd.DataFrame,
                      jaccard_threshold: float = 0.6) -> Dict:
    """
    Type-aware combined evaluation using original prf1_for_example.
    explicit/mixed/objective → gold=aspect, pred=pipeline
    implicit                 → gold=category aspect-part, pred=combined+KG
    """
    tot_tp = tot_fp = tot_fn = 0
    p_list, r_list, f_list = [], [], []
    for _, row in df.iterrows():
        gold = _get_gold_for_row(row)
        pred = _get_combined_pred_for_row(row)
        # Use original prf1_for_example for consistent matching
        m = prf1_for_example(gold, pred, thresh=jaccard_threshold)
        tot_tp += m['tp']; tot_fp += m['fp']; tot_fn += m['fn']
        p_list.append(m['precision'])
        r_list.append(m['recall'])
        f_list.append(m['f1'])
    mp = tot_tp/(tot_tp+tot_fp) if (tot_tp+tot_fp) else 0.0
    mr = tot_tp/(tot_tp+tot_fn) if (tot_tp+tot_fn) else 0.0
    mf = 2*mp*mr/(mp+mr)        if (mp+mr)         else 0.0
    return {
        "micro": {"P":round(mp,4),"R":round(mr,4),"F1":round(mf,4),
                  "TP":tot_tp,"FP":tot_fp,"FN":tot_fn},
        "macro": {"P":round(float(np.mean(p_list)),4),
                  "R":round(float(np.mean(r_list)),4),
                  "F1":round(float(np.mean(f_list)),4)},
    }

