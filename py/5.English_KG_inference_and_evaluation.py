_GENERAL_SYN = {
    "misc","miscellaneous","anecdotes/miscellaneous",
    "miscellany","other","general"
}
_IRREG = {
    "rooms":"room","prices":"price","drinks":"drink",
    "services":"service","facilities":"facility","amenities":"amenity",
}
_tok_re = re.compile(r"[a-z0-9]+")

def _sing(t):
    t = t.lower()
    if t in _IRREG: return _IRREG[t]
    if re.search(r'[^aeiou]ies$',t): return re.sub(r'ies$','y',t)
    if re.search(r'(sses|ches|shes|xes)$',t): return t[:-2]
    if t.endswith('s') and not t.endswith('ss') and len(t)>3: return t[:-1]
    return t

def _nf(s):
    return " ".join([_sing(x) for x in _tok_re.findall((s or "").lower())])

def _norm_asp_eval(s):
    n = _nf(s)
    return "general" if n in _GENERAL_SYN else n

def _head(s):
    toks = _nf(s).split()
    return toks[-1] if toks else ""

def _same_relaxed(a, b):
    a, b = _norm_asp_eval(a), _norm_asp_eval(b)
    if a == b: return True
    if _head(a) and _head(a) == _head(b): return True
    sa, sb = set(a.split()), set(b.split())
    if sa and sb and (sa.issubset(sb) or sb.issubset(sa)): return True
    return False

def _parse_gold_cat(val):
    if not isinstance(val, list): val = [str(val)]
    pairs = []
    for c in val:
        s = str(c).strip()
        if _is_null_value(s) or s == 'none': continue
        if '#' in s:
            e, a = s.split('#',1)
            pairs.append((_nf(e), _norm_asp_eval(a)))
        else:
            # ACOS format: 'food quality' / 'laptop operation_performance'
            tokens = s.strip().split()
            if len(tokens) >= 2:
                e   = _nf(tokens[0])
                asp = ' '.join(tokens[1:]).replace('_',' ')
                pairs.append((e, _norm_asp_eval(asp)))
            elif len(tokens) == 1:
                pairs.append(("*", _norm_asp_eval(tokens[0])))
    return pairs

def _parse_pred_pairs(val):
    if not isinstance(val, list): val = [str(val)]
    pairs = []
    for x in val:
        s = str(x).strip()
        if '#' in s:
            e, a = s.split('#',1)
            pairs.append((_nf(e), _norm_asp_eval(a)))
    return pairs

def _max_bip(adj, nl, nr):
    matchR = [-1]*nr
    def dfs(u, seen):
        for v in adj[u]:
            if not seen[v]:
                seen[v]=True
                if matchR[v]==-1 or dfs(matchR[v],seen):
                    matchR[v]=u; return True
        return False
    for u in range(nl): dfs(u,[False]*nr)
    return [(u,v) for v,u in enumerate(matchR) if u!=-1]

def evaluate_kg_pairs(df: pd.DataFrame,
                      pred_col: str = 'kg_final_pairs',
                      gold_col: str = 'category',
                      relaxed: bool = False) -> Dict:
    TP=FP=FN=any_tp=rows=0
    for _, row in df.iterrows():
        gold = _parse_gold_cat(row.get(gold_col,[]))
        if not gold: continue
        rows += 1
        pred = _parse_pred_pairs(row.get(pred_col,[]))
        if not pred: FN+=len(gold); continue
        adj = [
            [j for j,g in enumerate(gold)
             if (_same_relaxed(p[1],g[1]) if relaxed else p[1]==g[1])
             or (p[1]=="general" and g[1]=="general"
                 and g[0]!="*" and p[0]==g[0])]
            for p in pred
        ]
        mc = len(_max_bip(adj,len(pred),len(gold)))
        TP+=mc; FP+=len(pred)-mc; FN+=len(gold)-mc
        if mc>0: any_tp+=1
    p = TP/(TP+FP) if (TP+FP) else 0.0
    r = TP/(TP+FN) if (TP+FN) else 0.0
    f = 2*p*r/(p+r) if (p+r) else 0.0
    return {
        "P":round(p,4),"R":round(r,4),"F1":round(f,4),
        "TP":TP,"FP":FP,"FN":FN,"rows":rows,
        "Any-TP@row":round(any_tp/rows,4) if rows else 0.0,
    }

