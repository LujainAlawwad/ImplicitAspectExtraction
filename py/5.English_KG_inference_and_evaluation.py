    if not kg_preds_norm: return []
    kg_specific     = [a for a in kg_preds_norm if a != 'general']
    kg_only_general = len(kg_specific) == 0
    para_preds = row.get('cleaned_explicit_paraphrases', ['none'])
    if not isinstance(para_preds, list): para_preds = ['none']
    pipeline_has_preds = not all(
        _is_null_value(v) or v == 'none' for v in para_preds
    )
    if kg_only_general and pipeline_has_preds:
        return []
    return kg_preds_norm

