# ── Pattern 1: Anaphoric pronouns — refer to the product/entity ──
# When these are the nsubj of a copula, the sentence is IMPLICIT
# because the referent is not named in the sentence.
# e.g. "it is expensive" / "they are great" / "this is horrible"
ANAPHORIC_PRONOUNS: Set[str] = {
    "it", "its", "they", "them", "their",
    "this", "these", "that", "those",
    "everything", "nothing", "something",
}

# ── First-person pronouns — refer to the speaker, NOT the product ─
# When these are the nsubj, the sentence is about the speaker's
# experience, not an implicit product aspect.
# e.g. "I am satisfied" → speaker is satisfied, not the product
# These continue to BLOCK acomp (old behaviour preserved)
FIRST_PERSON_PRONOUNS: Set[str] = {
    "i", "we", "he", "she", "me", "us", "my", "our",
}


def find_lonely_adjectives_en(sentence: str) -> List[Dict]:
    if not sentence or not isinstance(sentence, str):
        return []

    doc    = _en_nlp(sentence)
    lonely = []
    seen   = set()

    for tok in doc:
        if tok.pos_ != "ADJ":
            continue
        if tok.dep_ in NON_OPINION_DEP_EN:
            continue
        if tok.lemma_.lower() in ADJ_SURFACE_BLOCKLIST:
            continue

        head = tok.head

        # ── acomp — Pattern 1 fix ────────────────────────────────
        # Climb up if head is ADJ (conj chain) to find the predicate
        if tok.dep_ == "acomp":
            governing_verb = head
            if governing_verb.pos_ == "ADJ":
                governing_verb = governing_verb.head

            if governing_verb.pos_ in ("AUX", "VERB"):
                nsubj_tokens = [
                    c for c in governing_verb.children
                    if c.dep_ in ("nsubj", "nsubjpass")
                ]
                for subj in nsubj_tokens:
                    subj_text = subj.text.lower()
                    if subj.pos_ in ("NOUN", "PROPN"):
                        # Named noun subject → explicit sentence → block
                        # e.g. "The pasta IS incredible" → block
                        break  # will continue outer loop after block
                    elif subj.pos_ == "PRON":
                        if subj_text in FIRST_PERSON_PRONOUNS:
                            # Speaker reference → block
                            # e.g. "I am satisfied" → block
                            break
                        elif subj_text in ANAPHORIC_PRONOUNS:
                            # Product anaphora → IMPLICIT → ALLOW
                            # e.g. "it is expensive" / "they are great"
                            # Do NOT break — let the adjective through
                            subj = None  # signal: allow
                            break
                        else:
                            # Unknown pronoun → conservative: block
                            break
                else:
                    subj = None  # no nsubj found → allow

                # If subj is not None at this point → block
                if subj is not None:
                    continue

        # ── conj — block if conjoined adj is acomp of verb ───────
        # with a NOUN/PROPN subject (explicit sentence)
        # If the conjoined acomp has only PRON subject → allow
        elif tok.dep_ == "conj":
            conj_head = head
            if conj_head.pos_ == "ADJ" and conj_head.dep_ == "acomp":
                pred = conj_head.head
                if pred.pos_ in ("AUX", "VERB"):
                    nsubj_tokens = [
                        c for c in pred.children
                        if c.dep_ in ("nsubj", "nsubjpass")
                    ]
                    block = False
                    for subj in nsubj_tokens:
                        if subj.pos_ in ("NOUN", "PROPN"):
                            block = True
                            break
                        elif subj.pos_ == "PRON":
                            if subj.text.lower() in FIRST_PERSON_PRONOUNS:
                                block = True
                                break
                            # Anaphoric PRON → allow (don't block)
                    if block:
                        continue

        # ── amod — allow ONLY if head noun is generic abstract ───
        elif tok.dep_ == "amod":
            if head and head.pos_ in ("NOUN", "PROPN"):
                head_lemma = (head.lemma_ or head.text).lower().strip()
                if head_lemma not in GENERIC_ABSTRACT_NOUNS:
                    continue
            else:
                continue

        # ── Other dep_rels — reject if direct NOUN/PROPN/NUM head ─
        elif head and head.i != tok.i:
            if head.pos_ in ("NOUN", "PROPN", "NUM"):
                continue

        # No NOUN/PROPN children
        if any(c.pos_ in ("NOUN", "PROPN") for c in tok.children):
            continue

        lemma = tok.lemma_.lower().strip()
        if not lemma or lemma in seen:
            continue
        seen.add(lemma)

        lonely.append({
            "surface":    tok.text,
            "lemma":      lemma,
            "dep_rel":    tok.dep_,
            "head_text":  head.text if head else "ROOT",
            "head_upos":  head.pos_ if head else "ROOT",
            "is_generic": lemma in GENERIC_SENT_EN,
        })

    return lonely

